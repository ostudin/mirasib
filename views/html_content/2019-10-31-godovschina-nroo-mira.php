<p>Новосибирская региональная общественная организация по защите прав и законных интересов инвалидов «МиРа» отметила годовщину своего существования. Возможно, кто-то скажет, что еще рано подводить итоги, но для всех членов НРОО «МиРа», это был не просто календарный год, а интересный, насыщенный и познавательный период становления организации.
</p>

