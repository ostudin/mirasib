<p>В приветственном слове основатель и президент НРОО “МиРа” Владимир Львович Кулекин вспомнил значимые вехи событий, произошедших за минувший год.<br/>
- Год назад, решив организационные вопросы по регистрации организации и финансированию ее деятельности, мы сразу же приступили к работе по защите прав и отстаиванию интересов людей с ограниченными возможностями здоровья в Новосибирской области. Подобрался надежный костяк, который сформировал работоспособную команду. Сегодня среди нас активные и инициативные люди, благодаря которым и осуществляется основная деятельность организации. Мы помогаем инвалидам с трудоустройством, ведем большую работу по их социализации. За минувший год было проведено более 70 мероприятий. Благодаря НРОО «МиРа» свыше 250 человек побывали в театрах города, около 500 человек неоднократно посещали Новосибирский зоопарк им. Р. А. Шило, бывали на экскурсиях в музеях, других общественных местах. Судя по объему работы за год, я с уверенностью могу отметить, что наша организация устремлена в будущее, у нас много планов и начинаний, которые хотелось бы реализовать. Поэтому сегодня мы не подводим итоги, а делимся со всеми тем, что нами было сделано за время работы организации.</p>

