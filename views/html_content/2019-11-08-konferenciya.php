<p>В свою очередь Андрей Травников подчеркнул, что поддержка “Единой России” важна в повседневной работе правительства области. «Обратная связь, полученная через местные и первичные организации Партии – это надежный и налаженный канал общения с жителями региона. Именно через них необходимо рассказывать людям о том, какие инициативы и проекты запущены и реализованы с подачи “Единой России”. Мы работаем для людей, мы слышим их нужды и включаемся в решение проблем на местах», - сказал он.</p>

