<p>Участвовавший в работе Конференции секретарь  Генсовета “Единой России” Андрей Турчак, напомнил, что ранее Андрей Травников решением Президиума Генерального совета Партии был согласован одним из 13 исполняющих обязанности секретарей региональных отделений. Тогда его кандидатуру одобрил лидер “Единой России” Дмитрий Медведев.</p>

<div class="blockquote">
	<p>«Это ответственное решение для всех нас. Глава региона – высшее должностное лицо, и когда он становится главой партии власти – это расширяет его ресурс для решения конкретных проблем людей. “Единая Россия” сегодня – миллионы людей и тысячи отделений по всей стране, она дает возможность услышать каждого и сделать еще больше для решения проблем людей на местах. Андрей Александрович возглавит нашу партийную команду в Новосибирской области и одновременно усилит региональное правительство. Я не сомневаюсь в том, что вы окажете ему необходимую поддержку в обновлении подходов к работе Партии в регионе. Как не сомневаюсь и в том, что его совместная с “Единой Россией” работа приведет к успешному результату Партии на выборах в Новосибирской области в следующем году», – сказал Андрей Турчак, обращаясь к делегатам Конференции.</p>
</div>

